//Arithmetic Operators 
//(+, -, *, /, %)
console.log(10 + 5);
console.log(10 - 5);
console.log(10 / 5);
console.log(10 * 5);
console.log(10 % 3);

//Comparison Operators 
//==, ===, !=
console.log(5 == "5");
console.log(5 === "5");
console.log(5 != 5);

//Logical Operators
// &&, ||, !
console.log(true && false);
console.log(true || false);
console.log(!true);

//Assignment Operators 
//=, +=, -=
let count = 10;
count += 5;
console.log(count);

//Ternary Operator 
let marks = 75;
let result = marks >= 35 ? "Pass" : "Fail";
console.log(result);